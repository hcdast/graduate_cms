<template>
    <div class="upload-container">
        <el-button icon='upload' :style="{background:color,borderColor:color}" @click=" dialogVisible=true"
                   type="primary">上传视频
        </el-button>
        <el-dialog v-model="dialogVisible">
            <el-upload
                    class="editor-slide-upload"
                    action="https://driver.dobell.me/driver/backend/uploadResourceFile"
                    drag
                    :multiple="false"
                    :data="uploadParam"
                    :on-remove="handleRemove"
                    :on-success="handleSuccess"
                    name="files"
                    :before-upload="beforeUpload">
                <el-button size="small" type="primary">点击上传</el-button>
            </el-upload>
            <el-button @click="dialogVisible = false">取 消</el-button>
            <el-button type="primary" @click="handleSubmit">确 定</el-button>
        </el-dialog>
    </div>
</template>
<script>
    import { mapGetters } from 'vuex'
    export default {
        name : 'vedioUpload',
        props : {
            color : {
                type : String,
                default : '#20a0ff'
            }
        },
        computed: {
            ...mapGetters([
                'aid'
            ])
        },
        data() {
            return {
                dialogVisible : false,
                uploadParam: {
                    aid: null,
                    type: 8
                },
                listObj : {},
                fileList : []
            }
        },
        methods : {
            checkAllSuccess() {
                return Object.keys ( this.listObj ).every ( item => this.listObj[ item ].hasSuccess )
            },
            handleSubmit() {
                const arr = Object.keys ( this.listObj ).map ( v => this.listObj[ v ] )
                if (! this.checkAllSuccess ()) {
                    this.$message ( '请等待所有视频上传成功 或 出现了网络问题，请刷新页面重新上传！' )
                    return
                }
                // console.log ( arr )
                this.$emit ( 'successCBK', arr )
                this.listObj = {}
                this.fileList = []
                this.dialogVisible = false
            },
            handleSuccess( response, file ) {
                // console.log(file)
                const uid = file.uid
                const objKeyArr = Object.keys ( this.listObj )
                for ( let i = 0, len = objKeyArr.length ; i < len ; i ++ ) {
                    if (this.listObj[ objKeyArr[ i ] ].uid === uid) {
                        this.listObj[ objKeyArr[ i ] ].url = response.data[0]
                        this.listObj[ objKeyArr[ i ] ].hasSuccess = true
                        return
                    }
                }
                // console.log(response)
            },
            handleRemove( file ) {
                const uid = file.uid
                const objKeyArr = Object.keys ( this.listObj )
                for ( let i = 0, len = objKeyArr.length ; i < len ; i ++ ) {
                    if (this.listObj[ objKeyArr[ i ] ].uid === uid) {
                        delete this.listObj[ objKeyArr[ i ] ]
                        return
                    }
                }
            },
            beforeUpload( file ) {
                this.uploadParam.aid = this.aid
                const _self = this
                const fileName = file.uid
                this.listObj[ fileName ] = {}
                _self.listObj[ fileName ] = {
                    hasSuccess : false,
                    uid : file.uid,
                }
                /*const _self = this
                const _URL = window.URL || window.webkitURL
                const fileName = file.uid
                this.listObj[ fileName ] = {}
                return new Promise ( ( resolve, reject ) => {
                    const img = new Image ()
                    img.src = _URL.createObjectURL ( file )
                    img.onload = function () {
                        _self.listObj[ fileName ] = {
                            hasSuccess : false,
                            uid : file.uid,
                            width : this.width,
                            height : this.height
                        }
                    }
                    resolve ( true )
                } )*/

            }
        }

    }
</script>

